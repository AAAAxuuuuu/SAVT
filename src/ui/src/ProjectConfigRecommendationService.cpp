QVariantMap ProjectConfigRecommendationService::buildRecommendation(
    const QString& projectRootPath,
    const savt::core::AnalysisReport& report,
    const savt::core::ArchitectureOverview&,
    const savt::core::CapabilityGraph& capabilityGraph) {
    QVariantMap recommendation;

    const QString cleanedRoot = QDir::cleanPath(projectRootPath.trimmed());
    const QFileInfo rootInfo(cleanedRoot);
    if (cleanedRoot.isEmpty() || !rootInfo.exists() || !rootInfo.isDir()) {
        recommendation.insert(QStringLiteral("available"), false);
        recommendation.insert(QStringLiteral("canGenerate"), false);
        recommendation.insert(QStringLiteral("summary"), QStringLiteral("先选择一个可访问的项目目录。"));
        recommendation.insert(QStringLiteral("targetPath"), QString());
        recommendation.insert(QStringLiteral("choiceItems"), QVariantList{});
        recommendation.insert(QStringLiteral("baseConfig"), QVariantMap{});
        recommendation.insert(QStringLiteral("draftConfig"), QVariantMap{});
        recommendation.insert(QStringLiteral("draftCounts"), QVariantMap{});
        recommendation.insert(QStringLiteral("draftJson"), QString());
        recommendation.insert(QStringLiteral("writeActionLabel"), QStringLiteral("生成配置"));
        recommendation.insert(QStringLiteral("tone"), QStringLiteral("info"));
        recommendation.insert(QStringLiteral("uncertainCount"), 0);
        return recommendation;
    }

    const auto existingConfig = savt::core::loadProjectAnalysisConfig(
        std::filesystem::path(QDir::fromNativeSeparators(cleanedRoot).toStdString()));
    const QString normalizedTargetPath =
        !existingConfig.configPath.empty()
            ? normalizePath(QString::fromStdString(existingConfig.configPath))
            : normalizePath(QDir(cleanedRoot).filePath(QStringLiteral(".savt/project.json")));

    const ScanResult scan = buildScanResult(cleanedRoot, report, capabilityGraph);

    RuleSet baseRules;
    for (const QString& directory : scan.highConfidenceIgnoreDirectories) {
        appendUniqueString(baseRules.ignoreDirectories, directory);
    }
    for (const QVariantMap& rule : scan.moduleMerges) {
        baseRules.moduleMerges.push_back(rule);
    }
    for (const QString& directory : scan.dependencyDirectories) {
        QVariantMap rule;
        rule.insert(QStringLiteral("match"), normalizePath(directory));
        rule.insert(QStringLiteral("collapse"), true);
        rule.insert(QStringLiteral("hideByDefault"), true);
        baseRules.dependencyFoldRules.push_back(rule);
    }

    const QVariantList choiceItems = buildChoiceItems(scan);
    recommendation.insert(QStringLiteral("available"), true);
    recommendation.insert(QStringLiteral("canGenerate"), true);
    recommendation.insert(QStringLiteral("projectKindLabel"), scan.projectKindLabel);
    recommendation.insert(QStringLiteral("targetPath"), nativePath(normalizedTargetPath));
    recommendation.insert(QStringLiteral("targetPathNormalized"), normalizedTargetPath);
    recommendation.insert(QStringLiteral("baseConfig"), ruleSetToVariantMap(baseRules));
    recommendation.insert(QStringLiteral("choiceItems"), choiceItems);
    recommendation.insert(
        QStringLiteral("writeActionLabel"),
        existingConfig.configPath.empty()
            ? QStringLiteral("生成配置")
            : QStringLiteral("重写配置"));
    recommendation.insert(QStringLiteral("hasExistingConfig"), !existingConfig.configPath.empty());
    recommendation.insert(QStringLiteral("existingConfigLoaded"), existingConfig.loaded);
    recommendation.insert(
        QStringLiteral("summary"),
        choiceItems.isEmpty()
            ? QStringLiteral("检测到 %1 工程。我已经整理出一份可以直接落盘的 SAVT 配置草案。")
                  .arg(scan.projectKindLabel)
            : QStringLiteral("检测到 %1 工程。我先补好了高置信规则，并把 %2 处拿不准的地方留成可切换选项。")
                  .arg(scan.projectKindLabel)
                  .arg(choiceItems.size()));
    return recomputeRecommendation(std::move(recommendation));
}

QVariantMap ProjectConfigRecommendationService::selectChoice(
    const QVariantMap& recommendation,
    const QString& choiceId,
    const QString& optionId) {
    QVariantMap updated = recommendation;
    QVariantList choices = updated.value(QStringLiteral("choiceItems")).toList();
    for (QVariant& choiceValue : choices) {
        QVariantMap choice = choiceValue.toMap();
        if (choice.value(QStringLiteral("id")).toString().trimmed() != choiceId.trimmed()) {
            continue;
        }

        bool optionFound = false;
        for (const QVariant& optionValue : choice.value(QStringLiteral("options")).toList()) {
            if (optionValue.toMap().value(QStringLiteral("id")).toString().trimmed() == optionId.trimmed()) {
                optionFound = true;
                break;
            }
        }
        if (!optionFound) {
            return recommendation;
        }

        choice.insert(QStringLiteral("selectedOptionId"), optionId.trimmed());
        choiceValue = choice;
        updated.insert(QStringLiteral("choiceItems"), choices);
        return recomputeRecommendation(std::move(updated));
    }

    return recommendation;
}

bool ProjectConfigRecommendationService::writeRecommendation(
    const QString& projectRootPath,
    const QVariantMap& recommendation,
    QString* writtenPath,
    QString* errorMessage) {
    if (writtenPath != nullptr) {
        writtenPath->clear();
    }
    if (errorMessage != nullptr) {
        errorMessage->clear();
    }

    const QString cleanedRoot = QDir::cleanPath(projectRootPath.trimmed());
    const QFileInfo rootInfo(cleanedRoot);
    if (cleanedRoot.isEmpty() || !rootInfo.exists() || !rootInfo.isDir()) {
        if (errorMessage != nullptr) {
            *errorMessage = QStringLiteral("当前项目目录不可访问，暂时没法写入配置文件。");
        }
        return false;
    }

    const QString draftJson = recommendation.value(QStringLiteral("draftJson")).toString();
    if (draftJson.trimmed().isEmpty()) {
        if (errorMessage != nullptr) {
            *errorMessage = QStringLiteral("当前草案为空，暂时没有可写入的配置内容。");
        }
        return false;
    }

    QString targetPath =
        normalizePath(recommendation.value(QStringLiteral("targetPathNormalized")).toString());
    if (targetPath.isEmpty()) {
        targetPath = normalizePath(QDir(cleanedRoot).filePath(QStringLiteral(".savt/project.json")));
    }

    const QFileInfo targetInfo(targetPath);
    QDir parentDir = targetInfo.dir();
    if (!parentDir.exists() && !QDir().mkpath(parentDir.absolutePath())) {
        if (errorMessage != nullptr) {
            *errorMessage = QStringLiteral("没能创建配置目录：%1")
                                .arg(QDir::toNativeSeparators(parentDir.absolutePath()));
        }
        return false;
    }

    QFile output(targetPath);
    if (!output.open(QIODevice::WriteOnly | QIODevice::Truncate | QIODevice::Text)) {
        if (errorMessage != nullptr) {
            *errorMessage = QStringLiteral("没能写入配置文件：%1")
                                .arg(QDir::toNativeSeparators(targetPath));
        }
        return false;
    }

    if (output.write(draftJson.toUtf8()) < 0) {
        if (errorMessage != nullptr) {
            *errorMessage = QStringLiteral("写入配置文件时失败了：%1")
                                .arg(QDir::toNativeSeparators(targetPath));
        }
        return false;
    }

    output.close();
    if (writtenPath != nullptr) {
        *writtenPath = QDir::toNativeSeparators(targetPath);
    }
    return true;
}

