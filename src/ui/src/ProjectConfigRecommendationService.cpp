QString normalizePath(QString path) {
    path = QDir::fromNativeSeparators(path.trimmed());
    while (path.startsWith(QStringLiteral("./"))) {
        path.remove(0, 2);
    }
    while (path.endsWith(QLatin1Char('/'))) {
        path.chop(1);
    }
    return path;
}

QStringList splitSegments(const QString& path) {
    return normalizePath(path).split(QLatin1Char('/'), Qt::SkipEmptyParts);
}

QString joinSegments(const QStringList& segments) {
    return normalizePath(segments.join(QLatin1Char('/')));
}

QString entryPrefixForFilePath(const QString& filePath) {
    QStringList segments = splitSegments(filePath);
    if (segments.size() <= 1) {
        return {};
    }

    segments.removeLast();
    if (segments.isEmpty()) {
        return {};
    }

    static const QSet<QString> genericTailSegments = {
        QStringLiteral("src"),
        QStringLiteral("source"),
        QStringLiteral("sources"),
        QStringLiteral("app"),
        QStringLiteral("apps"),
        QStringLiteral("bin"),
        QStringLiteral("java"),
        QStringLiteral("kotlin")
    };

    const QString tail = segments.constLast().trimmed().toLower();
    if (segments.size() > 1 && genericTailSegments.contains(tail)) {
        segments.removeLast();
    }
    return joinSegments(segments);
}

